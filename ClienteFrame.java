    private void btnConectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConectarActionPerformed
        // TODO add your handling code here:

        String nome = this.NomeTxt.getText();

        if (!nome.isEmpty()) {
            this.mensagem = new ChatDeMensagem();
            this.mensagem.setAction(Action.CONECTADO);
            this.mensagem.setNome(nome);

            this.servico = new ServicoCliente();
            this.socket = new ServicoCliente().conectar();
            new Thread(new ListenerSocket(this.socket)).start();

            this.servico.enviar(mensagem);
        }

    }//GEN-LAST:event_btnConectarActionPerformed

