    private class ListenerSocket implements Runnable {

        private ObjectInputStream input;

        public ListenerSocket(Socket socket) {
            try {
                this.input = new ObjectInputStream(socket.getInputStream());
            } catch (IOException ex) {
                Logger.getLogger(ClienteFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        @Override
        public void run() {

            ChatDeMensagem mensagem = null;

            try {

                while ((mensagem = (ChatDeMensagem) input.readObject()) != null) {
                    Action action = mensagem.getAction();

                    if (action.equals(Action.CONECTADO)) {
                        conectado(mensagem);

                    } else if (action.equals(Action.DESCONECTADO)) {
                        desconectar();
                        socket.close();
                    } else if (action.equals(Action.ENVIE_PARA_UMA_PESSOA)) {
                        receber(mensagem);
                    } else if (action.equals(Action.USUARIOS_ONLINE)) {
                        usuariosOn(mensagem);
                    }
                }
            } catch (IOException ex) {
                Logger.getLogger(ClienteFrame.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(ClienteFrame.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }

    private void conectado(ChatDeMensagem mensagem) {
        if (mensagem.getTexto().equals("NÃO")) {
            this.NomeTxt.setText("");
            JOptionPane.showMessageDialog(this, "Conexão não estabelecida\n Tente novamente com um outro nome");
            return;
        }
        this.mensagem = mensagem;
        this.btnConectar.setEnabled(false);
        this.NomeTxt.setEditable(false);
        this.btnSair.setEnabled(true);
        this.TextoEnvia.setEnabled(true);
        this.TextoRecebe.setEnabled(true);
        this.btnLimpar.setEnabled(true);
        
        JOptionPane.showMessageDialog(this, "Você esta conectado no chat");
    }

    private void desconectar() {

        this.btnConectar.setEnabled(true);
        this.NomeTxt.setEditable(true);

        this.btnSair.setEnabled(false);
        this.TextoEnvia.setEnabled(false);
        this.TextoRecebe.setEnabled(false);
        this.btnLimpar.setEnabled(false);
     
        this.TextoRecebe.setText("");
        this.TextoEnvia.setText("");
        JOptionPane.showMessageDialog(this, "Você saiu do chat");

    }

    private void receber(ChatDeMensagem mensagem) {
        this.TextoRecebe.append(mensagem.getNome() + "diz:" + mensagem.getTexto() + "\n");
    }

    private void usuariosOn(ChatDeMensagem mensagem) {
        
        
        System.out.println(mensagem.getUsuariosOnline().toString());
        Set<String> nome = mensagem.getUsuariosOnline();
        
        nome.remove((String)mensagem.getNome());
        
        String[] vet = (String[])nome.toArray(new String[nome.size()]);

        this.usuariosOn.setListData(vet);
        this.usuariosOn.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        this.usuariosOn.setLayoutOrientation(JList.VERTICAL);
    
    
    }   

